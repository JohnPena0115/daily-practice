// https://edabit.com/challenge/GLbuMfTtDWwDv2F73

function truthSeeker(array) {

    let truthInstances = 0;

    for (let i = 0; i < array.length; i++) {

        if (array[i] === true) {

            truthInstances++
        }


        return truthIntances
    }
}